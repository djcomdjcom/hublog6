      <p>■表題登記とは<br>
        一筆の土地または一個の建物に関して、最初になされる表示登記のこと。<br>
        新築された建物などの場合、登記記録そのものが存在していないので、登記記録そのものを新規に作成する手続きが必要になります。<br>
        この場合、新規に登記記録を作成するには手順として、まず表題部を作成する必要があり、このような登記を「表題登記」と呼んでいます。<br>
        建物の新築の場合、表題登記は建築後1ヵ月以内に申請しなければなりません。1ヵ月以内に申請しない場合は過料に処せられます。（ただし、1ヵ月経過後も表題登記の申請はでき、申請義務があります） </p>
      <p>■保存登記とは<br>
        所有権の保存の登記のことで、初めてする所有権の登記のこと。<br>
        登記記録上では、権利部の甲区（登記記録の不動産の所有権に関する事項を記載した部分）に「所有権保存　所有者A」のように記載されます。<br>
        所有権の保存の登記をすることができるのは、原則として、表題部所有者です（不動産登記法第74条）。</p>
