<section id="naibuketsuro" class="clearfix anchor">
  <h2 class="title " >内部結露</h2>
  <p class="title txt-lll">内部結露を防止して構造体を長持ちさせます</p>
  <p> 建物の気密性を向上し、スーパーウォールパネルで断熱することで、内部結露のリスクから構造体を守ります。<br>
    築25年の住宅を解体したら構造体が新築のようだったという事例もあります。 </p>
  <figure class="w100 maxw-890 mx-auto"> <img src="<?php bloginfo('stylesheet_directory'); ?>/images/builder/naibuketsuro001.jpg" alt="スーパーウォール工法の構造体写真"> </figure>
  <h3 class="">内部結露とは？</h3>
  <p> 壁の内部に湿気のある空気が通り抜ける部分があり、構造体内部（柱や構造壁）が結露してカビや腐食がおこると建物の耐久性は低くなります。<br>
    スーパーウォール工法は断熱性の高いパネルを隙間なく施工するため、構造内部に空気を通しませんので、構造体内部を結露させない工法を採用しています。 </p>
  <figure class="w100 maxw-890 mx-auto"> <img src="<?php bloginfo('stylesheet_directory'); ?>/images/builder/naibuketsuro001.png" alt="内部結露で傷んだ建物の写真"> </figure>
