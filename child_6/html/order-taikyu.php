<section id="kouzouzai" class="clearfix anchor">
  <h2 class="title" >構造材</h2>
  <h3>構造材とは？</h3>
  <p> 住宅の耐久性に大きく関係するのが、骨格となる構造材です。<br>
    仕上げ材は容易に改修できますが、構造改修となると、建て替えという事にもなりかねません。<br>
    構造材とは主に「土台と柱と梁」に使用する木材のこと言います。 </p>
  <h4 class="ttl brackets">土台</h4>
  <p> 地面に一番近く床下という環境なので、湿気による腐朽とシロアリの食害という危険に晒されます。腐朽に強くシロアリの食害を受けにくい■■■■■<span class="red">（ヒノキ／ヒバ／－ツガ）</span>を土台に使用しています。 </p>
  <h4 class="ttl brackets">柱</h4>
  <p> 柱には■■■■■を使用ています。<br>
    壁の内部結露による湿気、外壁部での漏水などが無いSW工法を採用し構造材の内部結露による劣化を35年保証しています。また長期的には、外壁のメンテナンス不足による雨水の侵入の可能性が考えられますので定期点検を実施しています。<br>
    地面に近い部分では土台と接していますから、湿気に強い事、シロアリに食われにくくする対策も施しています。 </p>
  <h4 class="ttl brackets">梁</h4>
  <p> 梁は柱の上に横たえて、屋根・棟の重みを受ける材木です。<br>
    垂直方向の荷重に耐えるために、高さのある材木を使用しますが、原木からそのサイズがとれないため、集成材を使用しています。<br>
    <span class="red">集成材</span>は強度にばらつきが少なく、大きな梁をつくることができます。 </p>
  <div class="bordrbox">
    <p class="title">参考文書</p>
    <p class="title brackets">ヒノキについて</p>
    <p> ヒノキは硬い材質でできているため、耐久性に優れ、腐朽菌にも強く長持する木材という特徴があります。ヒノキの特徴として、カビ・細菌繁殖の抑制、消臭・脱臭、湿度調整機能、断熱効果、などがあります。<br>
      伐採後から強度が増してゆき、100年はもつ木材としても知られています。 </p>
