
<h2 class="ttl point">
<span>
Point 1
</span>
地域密着工務店はメンテナンス力が強みです
</h2>


<p>新築注文住宅の建築から建物の修繕まで、当社は建築施工の専門家です。<br>
修繕工事は、この地域の建物の不具合について、日々多くのご依頼をいただき、施工実績を積み上げております。<br>
当社で新築させていただいた建物のアフターメンテナンスについても、地域特性を熟知している当社が、責任を持ってご対応させていただきますのでご安心ください。</p>

<hr>


<h2 class="ttl point">
<span>
Point 2
</span>
住み継ぐために大切な修繕計画
</h2>

<p>建物は年月を経ることで劣化していきますので、永く住み継ぐ住まいとして維持していくためには、適切な修繕が不可欠です。<br>
建物の性能をいつまでも維持することができれば、インテリアなどはライフスタイルに合わせてリフォームすることができます。<br>
特に屋根や外壁など構造部分に影響の大きい修繕は、計画的に実施する必要があります。<br>
当社では修繕計画に伴う修繕費用についても予め契約前にお伝えし、末永く安心して暮らせるアフターメンテナンスの実施をお勧めしております。</p>

<hr>

