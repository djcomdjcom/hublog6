<h2 class="ttl point">
<span>
Point 4
</span>
第三者機関による検査と安心の保証
</h2>

<p>当社は（財）住宅保証機構と（株）日本住宅保証検査機構（JIO）に加盟しています。当社が手掛けた住宅はすべて、信頼のおける第三者機関が検査をし、10年間の瑕疵担保責任保険の対象となります。</p>

<hr>

<h2 class="ttl point">
<span>
Point 5
</span>
地盤保証（工事期間中と完成後20年間）
</h2>

<p>家を建てる基礎となる地盤は、家の一部と言っていいほど重要です。しっかりとした地盤調査を行い、解析した上で保証を提供します。着工からの工事期間を含め、引き渡し日から20年間、地盤を保証いたします。</p>

<hr>

<h2 class="ttl point">
<span>
Point 6
</span>
白蟻保証（完成後10年間）
</h2>

万が一、竣工後10年以内に白蟻被害が発生した場合でも、修復費用が保証されます。

<hr>

<h2 class="ttl point">
<span>
Point 7
</span>
住宅履歴情報管理
</h2>

<p>住宅の設計・施工、維持管理、権利および資産に関する情報を、住宅履歴情報といいます。家がどのように建てられ、修繕され、改修・リフォームを行ったのかを記録する「家の履歴書」を、長期間蓄積いたします。お客様の大切な家の維持管理情報を蓄積し、資産価値を守ります。</p>

<hr>

<h2 class="ttl point">
<span>
Point 8
</span>
災害時緊急対応
</h2>

<p>地震や台風などの自然災害による緊急対応につきまして、当社で新築させていただきました建物を優先的にご対応させていただきます。<br>
応急処置や改修リフォームの保険適用など、災害時は当社にご相談ください。</p>

