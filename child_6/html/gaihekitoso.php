<section id="gaihekitoso02" class="clearfix anchor">
  <h2 class="mincho">外壁の劣化原因は？</h2>
  <div class="row mb-5">
    <div class="col-sm-6 col-12">
      <h3 class="ttl checked">強い日差し（紫外線や熱によるもの）</h3>
<p>
外壁塗装の表面が太陽光の紫外線や熱にさらされると、劣化の進行が早まります。<br>
特に真夏の紫外線の強さは強烈で、大きなダメージとなります。<br>
日当たりのよい建物の南面は、北面と比較して早期に劣化が生じやすくなります。<br>
北面の壁は日射しを浴びる時間が短い分、湿気を帯びやすくなります。<br>
そのため、カビや結露が発生しやすくなります。</p>

    </div>
    <div class="col-sm-6 col-12 "> <span class="w100"> <img src="<?php bloginfo('stylesheet_directory'); ?>/images/gaihekitoso/gaihekitoso001@2x.jpg" alt="外壁塗装の経年劣化のイメージ"/> </span> </div>
  </div>
	
  <div class="row mb-5">
    <div class="col-sm-6 col-12">
      <h3 class="ttl checked">雨や風（温暖差や雨水によるもの）</h3>
<p>
外壁は、常に雨や風にさらされることでダメージを受けやすくなります。<br>
また、強度を失った塗面からは、塗装の内側に雨水が徐々に侵入していきます。<br>
溜まった湿気は太陽の日射しにより温められると、体積が大きくなってしまいます。<br>
これにより、内側から押し出すように外壁が膨れてしまう症状も、経年劣化の症状の一つといえます。</p>

    </div>
    <div class="col-sm-6 col-12 "> <span class="w100"> <img src="<?php bloginfo('stylesheet_directory'); ?>/images/gaihekitoso/gaihekitoso002@2x.jpg" alt="外壁塗装の経年劣化のイメージ"/> </span> </div>
  </div>	
	
  <div class="row mb-5">
    <div class="col-sm-6 col-12">
      <h3 class="ttl checked">経年劣化</h3>
<p>
時間の経過とともに劣化が進行するのは避けられません。<br>
建物や外壁塗料には、耐用年数と呼ばれる経年劣化があります。<br>
建設場所が穏やかで良好な環境だったとしても、耐用年数を過ぎることで、経年劣化は進行してしまうものです。
</p>

    </div>
