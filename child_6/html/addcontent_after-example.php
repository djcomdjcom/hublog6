<h2 class="title " >FFC加工の免疫住宅</h2>

<p>
  クロスにFFC加工を標準仕様にすることで、美味しい空気の吸える化学物質やアレルギーのない住まい造りを実現しています。<br>
  「健康な家」、というと天然の木を沢山使った家などを思い浮かぶと思います。<br>
  しかし、その天然の木が、近年では世界各地で酸性雨が降り、汚染された状態にあるのです。<br>
  そこで、フローリングや内装部分に“天然の木の免疫力”を回復させたFFC加工材を使用することにより、リスクが改善されます。<br>
  FFCとは、水に溶ける特殊な鉄分（Ferrous Ferric Chloride/フェラス・フェリック・クロライド）です。これには、生体の分子構造を活性化させ、細胞組織を強固にするなど“生物の機能を高める”効果があることが多くの研究機関で確かめられています。
</p>


