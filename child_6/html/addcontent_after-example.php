<h2 class="title " >自然塗料（シオンのU-OIL）について</h2>
<p>
  塗料というと、鼻を刺すような独特なにおいを一度は嗅いだことがあるかと思います。種類によっては、塗料に有害な物質のシンナー等が含まれており、これを長時間吸い続けると、人体に健康被害の影響を及ぼす恐れがあります。
</p>

<p class="title brackets" >有害物質による人体への悪影響・主な症状</p>
<p>
<span class="check">
<span>吐き気</span><span>めまい</span><span>睡眠障害</span><span>頭痛</span><span>呼吸困難</span></span>　etc.
</p>

<p class="w100 w890">
  <img src="<?php bloginfo('stylesheet_directory'); ?>/images/builder/uoil001.jpg" alt="自然塗料の施工イメージ">
</p>


<p>
  シオンの自然塗料「U-OIL」は、自然素材である亜麻仁油、天然顔料等を活用しているので、においもほとんどなく、肌についても肌を痛めません。つまり、「安全で安心」な製品です。
</p>
<p>
  さらに、速乾、着色に優れた「作業性」、色落ちしにくい高い木材保護機能の「耐候性」を備えた「基本3性能（安全性・作業性・耐候性）」のすべてにおいて優位性が高い、本物の国産自然塗料です。
</p>

<p class="w100 w890">
