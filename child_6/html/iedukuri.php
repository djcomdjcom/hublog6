業者選びに関する正しい考え方
</h2>

<p>家づくりをはじめると、早々に行き詰まるのが業者選びです。<br>
ハウスメーカーにするか、ローコストビルダーにするか、地元の工務店にするか、設計事務所にするか、という選択肢で悩むところです。<br>
