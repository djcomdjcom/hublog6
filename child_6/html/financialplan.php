
<p class="lead">家づくりの資金計画は、一生涯にわたり家計を豊かにするチャンスです。</p>
<p>一生で一番大きな買い物ですから、上手に資金計画をすれば長期にわたる家計に余裕ができますし、無理な資金計画をすれば、旅行も外食も教育費もガマンばかりの人生になってしまいます。<br>
  ポイントは、<u>「建築費が高いか安いか」というような今だけのお金の動きに焦点を合わせないことです。</u><br>
  一生涯にわたって、理想的と思える暮らしをご家族みんなで考えて、それを実現するために何を優先し、お金を使うべきかを決めるために資金計画をしてください。<br>
  当社で行なう資金計画は、建物の見積金額と住宅ローンの借入金額の帳尻を合わせるものではありません。<br>
  家づくりの資金計画とは、いくらくらいの住宅ローンが組めるから、どのくらいの規模の建物が建てられるというような話だけではありません。<br>
  <u>子や孫に至るまで、豊かに暮らすためにご家族が優先すべき価値観を決めること</u>だと私たちは考えています。</p>
<p>当社では、ご家族の価値観を優先したお金の使い方を決めながら、<u>建築費、住宅ローン、補助金、税制優遇、ライフサイクルコスト、建物維持費など長期的な家計を計画</u>し、不確定な未来とは言え、お客様自身で見通しの明るい人生設計ができるように、家づくりの資金計画をさせていただきます。</p>
